def figure2_cs_discrimination():
    fig, ax = plt.subplots(figsize=(3.4, 1.72))
    schemes = ["Structural CS", "BiCheck CS"]
    correct = np.array([0.6592, 0.7888])
    wrong = np.array([0.6628, 0.7042])
    x = np.arange(2)

    width = 0.30
    ax.bar(x - width / 2, correct, width, color=BLUE, label="Correct", edgecolor="white", linewidth=0.4)
    ax.bar(x + width / 2, wrong, width, color=ORANGE, label="Wrong", edgecolor="white", linewidth=0.4)
    display_delta = [-0.0035, 0.0847]
    for xi, c, w, delta in zip(x, correct, wrong, display_delta):
        ax.text(xi, max(c, w) + 0.018, f"$\\Delta={delta:+.4f}$", fontsize=7, color=GREEN if delta > 0 else RED, ha="center")
        ax.text(xi - width / 2, c - 0.008, f"{c:.3f}", fontsize=6.2, color="white", ha="center", va="top")
        ax.text(xi + width / 2, w - 0.008, f"{w:.3f}", fontsize=6.2, color="white", ha="center", va="top")

    ax.set_xticks(x)
    ax.set_xticklabels(schemes)
    ax.set_ylabel("Mean consistency score")
    ax.set_ylim(0.60, 0.84)
    clean_axis(ax)
    ax.legend(frameon=False, loc="upper left", ncol=2, fontsize=7)
    save(fig, "image2")


def figure3_verification_unit():
    fig, ax = plt.subplots(figsize=(3.4, 1.58))
    ax.set_axis_off()
    ax.set_xlim(0, 1)
    ax.set_ylim(0, 1)

    rounded_box(ax, (0.04, 0.61), 0.20, 0.13, "Forward\n$q_i \\rightarrow a_i$", fc=BLUE_LIGHT, ec=BLUE, fontsize=7)
    rounded_box(ax, (0.04, 0.36), 0.20, 0.12, "Final $A$", fc="#F8FBFF", ec=NAVY, fontsize=7)
    rounded_box(ax, (0.04, 0.17), 0.20, 0.10, "Context $C$", fc=LIGHT_GRAY, ec=MID_GRAY, fontsize=7)

    rounded_box(ax, (0.38, 0.43), 0.23, 0.17, "Re-derive\n$(q_i,A,C) \\rightarrow a'_i$", fc=ORANGE_LIGHT, ec=ORANGE, fontsize=7)
    rounded_box(ax, (0.75, 0.55), 0.18, 0.11, "Forward $a_i$", fc=BLUE_LIGHT, ec=BLUE, fontsize=7)
    rounded_box(ax, (0.75, 0.34), 0.18, 0.11, "Backward $a'_i$", fc=ORANGE_LIGHT, ec=ORANGE, fontsize=7)
    rounded_box(ax, (0.75, 0.15), 0.18, 0.10, "match: 0/1", fc="white", ec=MID_GRAY, fontsize=7)

    arrow(ax, (0.24, 0.66), (0.38, 0.52), NAVY)
    arrow(ax, (0.24, 0.36), (0.38, 0.50), NAVY)
    arrow(ax, (0.24, 0.19), (0.38, 0.48), NAVY)
    arrow(ax, (0.61, 0.51), (0.75, 0.395), ORANGE)
    arrow(ax, (0.84, 0.55), (0.84, 0.45), GRAY)
    arrow(ax, (0.84, 0.34), (0.84, 0.25), GRAY)
    ax.text(0.50, 0.77, "independent re-derivation", ha="center", fontsize=6.8, color=ORANGE)
    ax.text(0.50, 0.045, r"$S_{crossval}=\sum_i w_i\,match(a_i,a'_i)/\sum_i w_i$", ha="center", fontsize=7.2, color=GREEN)
    save(fig, "image3")


def figure5_2wiki_boundary():
    fig, ax = plt.subplots(figsize=(3.4, 2.05))
    types = ["comparison (n=25)", "bridge-comp. (n=21)", "compositional (n=39)", "inference (n=15)"]
    cot = np.array([0.815, 0.905, 0.284, 0.361])
    gers = np.array([0.775, 0.524, 0.297, 0.288])
    y = np.arange(len(types))
    height = 0.32
    ax.axhspan(0.5, 1.5, color=ORANGE_LIGHT, zorder=0)
    ax.barh(y - height / 2, cot, height, label="Standard CoT", color="#B9B9B9", edgecolor="white", linewidth=0.5)
    ax.barh(y + height / 2, gers, height, label="GERS-DAG", color=BLUE, edgecolor="white", linewidth=0.5)
    for yi, c, g in zip(y, cot, gers):
        ax.text(c + 0.015, yi - height / 2, f"{c:.3f}", va="center", fontsize=6.2)
        ax.text(g + 0.015, yi + height / 2, f"{g:.3f}", va="center", fontsize=6.2)
    ax.text(
        0.72,
        1.12,
        "$\\Delta=-0.381$",
        color=TEXT,
        fontsize=7,
        fontweight="bold",
        ha="center",
        bbox=dict(facecolor="white", edgecolor="none", pad=0.5),
    )
    ax.set_xlabel("F1")
    ax.set_xlim(0, 1.02)
    ax.set_yticks(y)
    ax.set_yticklabels(types, fontsize=7)
    ax.invert_yaxis()
    ax.grid(axis="x", color=GRID, lw=0.45)
    ax.legend(frameon=False, loc="lower right", fontsize=7)
    save(fig, "image5")


def main():
    figure1_framework()
